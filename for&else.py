## using else in for loop ...
yadav = ['Ayush','Rishi','Kaalu','Bastard']

'''for loop can end in following ways :-
- when the iterator is ended.
- when you reach at the last letter of string.
- when there is a break statement.'''

#python control will fall in else when for loop is ended. 

for i in yadav:
    print(i,':')
    
    
else:
    print("For loop ended:")